public class Question3 {
    public static void main(String[] args) {

        int n1 = 5;
        int n2 = 10;
        double q = (double) n2 / n1;
        double rem = (double) n2 % n1;
        System.out.println("Quotient on dividing " + n2 + " by " + n1 + " = " + q);
        System.out.println("Remainder on dividing " + n2 + " by " + n1 + " = " + rem);
        System.out.println();

    }
}
